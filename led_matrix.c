// Função para converter brilho e cor em um valor 32 bits
uint32_t calcular_rgb(uint8_t r, uint8_t g, uint8_t b, double brilho)
{
    uint8_t R = (uint8_t)(r * brilho);
    uint8_t G = (uint8_t)(g * brilho);
    uint8_t B = (uint8_t)(b * brilho);
    return (G << 24) | (R << 16) | (B << 8);
}

// Inicializa a matriz de LEDs
void setup_led_matrix() {
    bool ok;
    // Configura o clock para 128 MHz
    ok = set_sys_clock_khz(133000, false);
    stdio_init_all();

    printf("Iniciando a transmissão PIO\n");
    if (ok) printf("Clock configurado para %ld Hz\n", clock_get_hz(clk_sys));

    // Configuração do PIO
    uint offset = pio_add_program(pio, &pio_matrix_program);
    uint sm = pio_claim_unused_sm(pio, true);
    pio_matrix_program_init(pio, sm, offset, OUT_PIN);
    desenho_on(desenhopadrao, pio, sm, 1.0, 1.0, 1.0);

}

void bin_printf(int num) {
    for (int i = 31; i >= 0; i--) {
        (num & (1 << i)) ? printf("1") : printf("0");
    }
}

double convertDrawingToMatrixIndex(double *desenho, int index) {
    int actualRow = index / COLUMNS;
    int actualColumn = index % COLUMNS;
    if (actualRow % 2) {
        return desenho[(actualRow * COLUMNS) + (COLUMNS - 1 - actualColumn)];
    }
    return desenho[index];
}

void draw(double **desenhoRGB) {
    uint32_t led_value;
    for (int16_t i = 0; i < NUM_LEDS; i++) {
        led_value = matrix_rgb(
            1.0, // Aqui você define o brilho. Altere o valor conforme necessário.
            convertDrawingToMatrixIndex(desenhoRGB[0], NUM_LEDS - 1 - i),
            convertDrawingToMatrixIndex(desenhoRGB[1], NUM_LEDS - 1 - i),
            convertDrawingToMatrixIndex(desenhoRGB[2], NUM_LEDS - 1 - i)
        );
        pio_sm_put_blocking(pio0, sm, led_value);
    }
    bin_printf(led_value);
}

void animate(double ***movie, uint frames, uint sleep_time) {
    for (int i = 0; i < frames; i++) {
        draw(movie[i]);
        sleep_ms(sleep_time);
    }
}

